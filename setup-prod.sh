#!/bin/bash

# NeoVantis Notification Service - Production Setup Script
# This script sets up the production environment for the notification service

set -e

echo "🚀 Setting up NeoVantis Notification Service - Production Environment"
echo "====================================================================="

# Colors for output
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
BLUE='\033[0;34m'
NC='\033[0m' # No Color

# Function to print colored output
print_status() {
    echo -e "${BLUE}[INFO]${NC} $1"
}

print_success() {
    echo -e "${GREEN}[SUCCESS]${NC} $1"
}

print_warning() {
    echo -e "${YELLOW}[WARNING]${NC} $1"
}

print_error() {
    echo -e "${RED}[ERROR]${NC} $1"
}

# Check if Docker is installed and running
check_docker() {
    print_status "Checking Docker installation..."
    if ! command -v docker &> /dev/null; then
        print_error "Docker is not installed. Please install Docker first."
        exit 1
    fi
    
    if ! docker info &> /dev/null; then
        print_error "Docker is not running. Please start Docker first."
        exit 1
    fi
    
    print_success "Docker is installed and running"
}

# Check if Docker Compose is installed
check_docker_compose() {
    print_status "Checking Docker Compose installation..."
    if ! command -v docker-compose &> /dev/null; then
        print_error "Docker Compose is not installed. Please install Docker Compose first."
        exit 1
    fi
    print_success "Docker Compose is installed"
}

# Generate secure passwords
generate_passwords() {
    print_status "Generating secure passwords..."
    
    if ! command -v openssl &> /dev/null; then
        print_error "OpenSSL is required for password generation. Please install it first."
        exit 1
    fi
    
    DB_PASSWORD=$(openssl rand -base64 32)
    REDIS_PASSWORD=$(openssl rand -base64 32)
    API_KEY=$(openssl rand -base64 32)
    
    print_success "Secure passwords generated"
}

# Create necessary directories
create_directories() {
    print_status "Creating necessary directories..."
    mkdir -p logs
    mkdir -p logs/nginx
    mkdir -p scripts
    mkdir -p config
    mkdir -p config/ssl
    print_success "Directories created"
}

# Create production environment file
create_env_file() {
    print_status "Creating production environment file..."
    
    read -p "Enter your Mailcow password for no-reply@neovantis.xyz: " -s MAIL_PASS
    echo ""
    
    read -p "Enter CORS origins (comma-separated, e.g., https://app.neovantis.xyz,https://admin.neovantis.xyz): " CORS_ORIGIN
    
    read -p "Enter Sentry DSN (optional, press enter to skip): " SENTRY_DSN
    
    cat > .env.prod << EOF
# Production Environment Configuration
# Auto-generated by setup-prod.sh

# Database Configuration
DB_PASSWORD=${DB_PASSWORD}

# Redis Configuration
REDIS_PASSWORD=${REDIS_PASSWORD}

# Email Configuration (Mailcow)
MAIL_PASS=${MAIL_PASS}

# Security Configuration
API_KEY=${API_KEY}
CORS_ORIGIN=${CORS_ORIGIN}

# Monitoring (optional)
SENTRY_DSN=${SENTRY_DSN}
EOF
    
    print_success "Production environment file created at .env.prod"
    print_warning "Keep .env.prod secure and never commit it to version control!"
}

# Create PostgreSQL configuration
