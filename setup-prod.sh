# Main execution
main() {
    echo "Starting production environment setup..."
    echo ""
    
    print_warning "This script will set up a production environment with secure passwords."
    print_warning "Make sure you're running this on a production server."
    read -p "Continue? (y/N): " -n 1 -r
    echo ""
    if [[ ! $REPLY =~ ^[Yy]$ ]]; then
        print_error "Setup cancelled."
        exit 1
    fi
    
    check_docker
    check_docker_compose
    generate_passwords
    create_directories
    create_env_file
    create_postgres_config
    create_redis_config
    create_nginx_config
    create_db_init_script
    create_backup_script
    start_services
    wait_for_services
    run_migrations
    setup_log_rotation
    setup_cron_jobs
    display_info
    
    print_success "Production setup completed successfully! 🎉"
}

# Handle script interruption
