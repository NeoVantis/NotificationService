track_activity_query_size = 2048
pg_stat_statements.track = all
EOF
    
    print_success "PostgreSQL configuration created"
}

# Create Redis configuration
create_redis_config() {
    print_status "Creating Redis configuration..."
    
    cat > config/redis.conf << 'EOF'
# Redis Configuration for Production
# Optimized for performance and persistence

# Network
bind 0.0.0.0
port 6379
timeout 300
tcp-keepalive 300

# Memory Management
maxmemory 512mb
maxmemory-policy allkeys-lru

# Persistence
save 900 1
save 300 10
save 60 10000
stop-writes-on-bgsave-error yes
rdbcompression yes
rdbchecksum yes
dbfilename dump.rdb

# Logging
loglevel notice
logfile /var/log/redis/redis-server.log

# Security
rename-command FLUSHDB ""
rename-command FLUSHALL ""
rename-command KEYS ""
rename-command CONFIG "CONFIG_b835729d"

# Performance
tcp-backlog 511
databases 16
EOF
    
    print_success "Redis configuration created"
}

# Create Nginx configuration
create_nginx_config() {
    print_status "Creating Nginx configuration..."
    
    cat > config/nginx.conf << 'EOF'
events {
    worker_connections 1024;
}

http {
    include /etc/nginx/mime.types;
    default_type application/octet-stream;
    
    # Logging
    log_format main '$remote_addr - $remote_user [$time_local] "$request" '
                    '$status $body_bytes_sent "$http_referer" '
                    '"$http_user_agent" "$http_x_forwarded_for"';
    
    access_log /var/log/nginx/access.log main;
    error_log /var/log/nginx/error.log warn;
    
    # Basic Settings
    sendfile on;
    tcp_nopush on;
    tcp_nodelay on;
    keepalive_timeout 65;
    types_hash_max_size 2048;
    client_max_body_size 16M;
    
    # Security Headers
    add_header X-Frame-Options DENY;
    add_header X-Content-Type-Options nosniff;
    add_header X-XSS-Protection "1; mode=block";
    add_header Strict-Transport-Security "max-age=31536000; includeSubDomains; preload";
    
    # Gzip Compression
    gzip on;
    gzip_vary on;
    gzip_min_length 10240;
    gzip_proxied expired no-cache no-store private must-revalidate max-age=0;
    gzip_types
        application/json
        application/javascript
        application/xml+rss
        application/atom+xml
        image/svg+xml
        text/plain
        text/css
        text/js
        text/xml
        text/javascript;
    
    # Rate Limiting
    limit_req_zone $binary_remote_addr zone=api:10m rate=1000r/h;
    
    # Upstream
    upstream notification_service {
        server app:4321 max_fails=3 fail_timeout=30s;
    }
    
    # HTTP Server (redirect to HTTPS)
    server {
        listen 80;
        server_name notifications.neovantis.xyz;
        return 301 https://$server_name$request_uri;
    }
    
    # HTTPS Server
    server {
        listen 443 ssl http2;
        server_name notifications.neovantis.xyz;
        
        # SSL Configuration
        ssl_certificate /etc/ssl/certs/neovantis.xyz.crt;
        ssl_certificate_key /etc/ssl/private/neovantis.xyz.key;
        ssl_protocols TLSv1.2 TLSv1.3;
        ssl_ciphers ECDHE-RSA-AES256-GCM-SHA512:DHE-RSA-AES256-GCM-SHA512:ECDHE-RSA-AES256-GCM-SHA384:DHE-RSA-AES256-GCM-SHA384;
        ssl_prefer_server_ciphers off;
        ssl_session_cache shared:SSL:10m;
        ssl_session_timeout 10m;
        
        # API Routes
        location / {
            limit_req zone=api burst=20 nodelay;
            
            proxy_pass http://notification_service;
            proxy_set_header Host $host;
            proxy_set_header X-Real-IP $remote_addr;
            proxy_set_header X-Forwarded-For $proxy_add_x_forwarded_for;
            proxy_set_header X-Forwarded-Proto $scheme;
            
            # Timeouts
            proxy_connect_timeout 30s;
            proxy_send_timeout 30s;
            proxy_read_timeout 30s;
            
            # Health check bypass
            if ($request_uri = "/api/v1/health/simple") {
                access_log off;
            }
        }
        
        # Health check endpoint (no rate limiting)
        location /api/v1/health {
            proxy_pass http://notification_service;
            proxy_set_header Host $host;
            proxy_set_header X-Real-IP $remote_addr;
            proxy_set_header X-Forwarded-For $proxy_add_x_forwarded_for;
            proxy_set_header X-Forwarded-Proto $scheme;
            access_log off;
        }
    }
}
EOF
    
    print_success "Nginx configuration created"
    print_warning "Please place your SSL certificates in config/ssl/ directory"
}

# Create database initialization script
create_db_init_script() {
    print_status "Creating database initialization script..."
    
    cat > scripts/init-db.sql << 'EOF'
-- Database initialization script for NeoVantis Notification Service (Production)
-- This script sets up the database with proper permissions

-- Create extensions if they don't exist
CREATE EXTENSION IF NOT EXISTS "uuid-ossp";

-- Grant necessary permissions
GRANT ALL PRIVILEGES ON DATABASE notifications_prod TO notifications_user;

-- Create additional indexes for performance
-- These will be created by TypeORM migrations, but can be added here if needed
EOF
    
    print_success "Database initialization script created"
}

# Create backup script
create_backup_script() {
    print_status "Creating backup script..."
    
    cat > scripts/backup-db.sh << 'EOF'
#!/bin/bash

# Database backup script for NeoVantis Notification Service

BACKUP_DIR="/opt/backups/notifications"
DATE=$(date +%Y%m%d_%H%M%S)
BACKUP_FILE="notifications_backup_${DATE}.sql"

# Create backup directory if it doesn't exist
mkdir -p ${BACKUP_DIR}

# Perform backup
docker exec notifications_postgres_prod pg_dump -U notifications_user notifications_prod > ${BACKUP_DIR}/${BACKUP_FILE}

# Compress backup
gzip ${BACKUP_DIR}/${BACKUP_FILE}

# Remove backups older than 30 days
find ${BACKUP_DIR} -name "*.gz" -mtime +30 -delete

echo "Backup completed: ${BACKUP_DIR}/${BACKUP_FILE}.gz"
EOF
    
    chmod +x scripts/backup-db.sh
    print_success "Backup script created at scripts/backup-db.sh"
}

# Build and start services
start_services() {
    print_status "Building and starting production services..."
    
    # Load environment variables
    export $(cat .env.prod | grep -v '^#' | xargs)
    
    # Build and start services
    docker-compose -f docker-compose.prod.yml up -d --build
    
    print_success "Services started successfully"
}

# Wait for services to be healthy
wait_for_services() {
    print_status "Waiting for services to be ready..."
    
    # Wait for PostgreSQL
    print_status "Waiting for PostgreSQL..."
    until docker exec notifications_postgres_prod pg_isready -U notifications_user -d notifications_prod; do
        sleep 2
    done
    print_success "PostgreSQL is ready"
    
    # Wait for Redis
    print_status "Waiting for Redis..."
    until docker exec notifications_redis_prod redis-cli --no-auth-warning -a ${REDIS_PASSWORD} ping; do
        sleep 2
    done
    print_success "Redis is ready"
    
    # Wait for application
    print_status "Waiting for Notification Service..."
    for i in {1..30}; do
        if curl -s http://localhost:4321/api/v1/health/simple > /dev/null; then
            print_success "Notification Service is ready"
            break
        fi
        if [ $i -eq 30 ]; then
            print_error "Timeout waiting for Notification Service"
            exit 1
        fi
        sleep 3
    done
}

# Run database migrations
run_migrations() {
    print_status "Running database migrations..."
    docker exec notifications_app_prod npm run migration:run
    print_success "Database migrations completed"
}

# Setup log rotation
setup_log_rotation() {
    print_status "Setting up log rotation..."
    
    cat > /etc/logrotate.d/notifications << EOF
/path/to/NotificationService/logs/*.log {
    daily
    rotate 30
    compress
    delaycompress
    missingok
    notifempty
    create 644 root root
    postrotate
        docker kill --signal="USR1" notifications_app_prod 2>/dev/null || true
    endscript
}
EOF
    
    print_success "Log rotation configured"
}

# Setup monitoring cron jobs
setup_cron_jobs() {
    print_status "Setting up monitoring cron jobs..."
    
    # Create cron jobs for production monitoring
    (crontab -l 2>/dev/null; echo "0 2 * * * /path/to/NotificationService/scripts/backup-db.sh") | crontab -
    (crontab -l 2>/dev/null; echo "*/5 * * * * curl -s http://localhost:4321/api/v1/health/simple > /dev/null || echo 'Service down' | mail -s 'Notification Service Alert' admin@neovantis.xyz") | crontab -
    
    print_success "Cron jobs configured"
}

